        boolean check=false;
        String tcategory = jTextField12.getText();
        if (tcategory.isEmpty()) {
                JOptionPane.showMessageDialog(this, "Enter a category!");
            }
            try {
                BufferedReader fileInputStream = new BufferedReader(new FileReader("favBooks.txt"));
                String currentLine;
                ArrayList<String> list = new ArrayList();
                while ((currentLine = fileInputStream.readLine()) != null) {
                    String arr[] = currentLine.split(", ");
                    String category = arr[2].toLowerCase();
                    if (category.contains(tcategory.toLowerCase())) {
                        list.add(arr[5] + arr[1]);
                    }
                }
                String[] arr = new String[list.size()];
                for (int i = 0; i < list.size(); i++) {
                    arr[i] = list.get(i);
                }
                sort(arr);
                for (String title : arr) {
                    jTextArea5.append("Title: " + title.substring(1) + "\n");
                    jTextArea5.append("\n");
                    check=true;
                }
                if(check==false){
                    JOptionPane.showMessageDialog(this, "There are no such books in category " + tcategory);
                }
                fileInputStream.close();
            } catch (FileNotFoundException e) {
                System.out.println("File not found.");
            } catch (IOException e) {
                e.printStackTrace();
                System.out.println("IOException");
            }
